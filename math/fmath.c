#include <math.h>

double _fcos(double f)
{
	return cos(f);
}

double _facos(double f)
{
	return acos(f);
}

double _fsin(double f)
{
	return sin(f);
}

double _fasin(double f)
{
	return asin(f);
}

double _ftan(double f)
{
	return tan(f);
}

double _fatan(double f)
{
	return atan(f);
}
